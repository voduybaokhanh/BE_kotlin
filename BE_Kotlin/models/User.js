const mongoose = require("mongoose");

// Định nghĩa schema cho User
const UserSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
});

// Tạo và xuất model
module.exports = mongoose.model("User", UserSchema);
